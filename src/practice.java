




//Sum of First N natural numbers in java
import java.util.Scanner;
class practice{
    public static void main(String[]args){
        Scanner s=new Scanner(System.in);
        int a=s.nextInt();
        int sum=0;
        for(int i=1;i<=a;i++){
            sum=sum+i;
        }
        System.out.println("the sum is "+sum);
    }
}
















