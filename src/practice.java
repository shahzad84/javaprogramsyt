

//Java Program to Swap Two Numbers

class practice{
    static void swaps(int a,int b){
        int temp=a;
        a=b;
        b=temp;
        System.out.println("The a after swap is : "+a + "the b after swap is : "+ b);
    }
    public static void main(String[]args){
        int a=3,b=9;
        swaps(a,b);
    }

}








